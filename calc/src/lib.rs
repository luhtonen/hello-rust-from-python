// build calculator functions

// add two number
pub fn add(a: i32, b: i32) -> i32 {
    a + b
}

// subtract two number
pub fn subtract(a: i32, b: i32) -> i32 {
    a - b
}

// multiply two number
pub fn multiply(a: i32, b: i32) -> i32 {
    a * b
}

// devide two number
pub fn devide(a: i32, b: i32) -> i32 {
    a / b
}
